# This program demonstrates the insert method

def main():
	# Create a list with some names
	names = ['James', 'Kathryn', 'Bill']

	# Display the list
	print('The list before inserting new name:')
	print(names)
	print()

	# Insert a new name at element 0
	names.insert(0, 'Tunde Muhamed')

	# Display the list again
	print('The list after inserting new name:')
	print(names)

main()
